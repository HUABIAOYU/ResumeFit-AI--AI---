# 构建无空格索引
for raw, label in _raw_map:
    SECTION_LABEL_MAP[raw.replace(" ", "")] = label

# 按长度降序排列关键词（优先匹配长的）
_KEYWORDS_SORTED = sorted(SECTION_LABEL_MAP.keys(), key=len, reverse=True)

# 构建匹配 pattern
_SECTION_PATTERN = re.compile(
    r"^[【\[]?\s*(" + "|".join(re.escape(kw) for kw in _KEYWORDS_SORTED) + r")[】\]\s：:]*\s*$",
    re.MULTILINE,
)


def segment_resume_text(text: str) -> List[dict]:
    """将简历全文按段落标题拆分为结构化分段"""
    matches = list(_SECTION_PATTERN.finditer(text))

    if not matches:
        if text.strip():
            return [{"section": "其他", "text": text.strip()}]
        return []

    segments: List[dict] = []

    # 第一个标题之前的内容视为"个人信息"
    first = matches[0]
    pre = text[:first.start()].strip()
    if pre:
        segments.append({"section": "个人信息", "text": pre})

    for i, m in enumerate(matches):
        raw = re.sub(r"\s+", "", m.group(1))
        section = SECTION_LABEL_MAP.get(raw, "其他")

        start = m.end()
        end = matches[i + 1].start() if i + 1 < len(matches) else len(text)
        body = text[start:end].strip()

        if body:
            segments.append({"section": section, "text": body})

    return segments
